    private void bAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAddActionPerformed
        // TODO add your handling code here:
        String cedula = tCedula.getText();
        String nombre = tNombre.getText();
        String apellido = tApellido.getText();
        String email = tEmail.getText();
        String direccion = tDireccion.getText();
        String login = tUsuario.getText();
        String contrasena = tPassword.getText();
        
        Usuarios nuevoUsuario = new Usuarios(cedula, nombre, apellido, email, login, contrasena, direccion);
        gestionarUsuario usuarioService = new gestionarUsuario();
        
        // Verificar si el usuario ya existe
        if (usuarioService.existeUsuario(cedula)) {
            JOptionPane.showMessageDialog(this, "El usuario ya existe.");
        } else {
            // Agregar el nuevo usuario si no existe
            if (usuarioService.agregarUsuario(nuevoUsuario)) {
                JOptionPane.showMessageDialog(this, "Usuario agregado exitosamente");

                // Llama al m√©todo listarTablaUsuarios en la instancia principal
                if (principal != null) {
                    principal.listarTablaUsuarios();
                }
            dispose(); // Cierra el formulario
            } else {
                JOptionPane.showMessageDialog(this, "Error al agregar usuario");
            }
        }
    }//GEN-LAST:event_bAddActionPerformed

